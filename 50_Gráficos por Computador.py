import matplotlib.pyplot as plt

# Datos para graficar
x = [1, 2, 3, 4, 5]
y = [2, 3, 5, 7, 11]

# Graficar los datos
plt.plot(x, y, marker='o', linestyle='-')

# Personalizar el gráfico
plt.title('Gráfico por Computador')
plt.xlabel('Eje X')
plt.ylabel('Eje Y')

# Mostrar el gráfico
plt.grid(True)
plt.show()





























